package jwd.banka.web.dto;

public class PrenosDTO {

	private String saRacun;
	private String naRacun;
	private Double iznos;

	public String getSaRacun() {
		return saRacun;
	}

	public void setSaRacun(String saRacun) {
		this.saRacun = saRacun;
	}

	public String getNaRacun() {
		return naRacun;
	}

	public void setNaRacun(String naRacun) {
		this.naRacun = naRacun;
	}

	public Double getIznos() {
		return iznos;
	}

	public void setIznos(Double iznos) {
		this.iznos = iznos;
	}

}
